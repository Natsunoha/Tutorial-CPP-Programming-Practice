#include <iostream>
using namespace std;

int main()
{
	cout << "\t There was once a kitten named Cheeses." << endl;
	cout << "Cheeses was the best kitten ever.  He ran and sang and danced all day!" << endl;
	cout << "Cheeses loved his toy Octopus, Squeakers." << endl << endl;
	
	cout << "\t One day, Cheeses fell down a cliff and landed in a volcano." << endl;
	cout << "His best toy friend Squeakers mourned Cheeses all week." << endl;
	cout << "But when Cheeses came back, Squeakers was happy!" << endl << endl;
	
	cout << "\t Turns out, Cheeses was actually a figment of Squeakers' imagination!" << endl;
	cout << "Cheeses and Squeakers lived happily ever after!" << endl;
	
	return 0;
}
